class Solution {
  public:
    int romanToDecimal(string &str) {
        // code here
        unordered_map<char,int> umap;
        
        umap['I'] = 1;
        umap['V'] = 5;
        umap['X'] = 10;
        umap['L'] = 50;
        umap['C'] = 100;
        umap['D'] = 500;
        umap['M'] = 1000;
        
        int ans =0 ;
        for(int i=0;i<str.length();i++){
            if(i+1<str.length() && umap[str[i]]<umap[str[i+1]]){
                ans-= umap[str[i]];
            } else{
                ans+= umap[str[i]];
            }
        }
        return ans;
    }
};

